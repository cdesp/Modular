ORG 9000

INCLUDE ..\MYOS_2_0000.SYM




MYTST:	LD B,10
DONXT:	PUSH BC
	CALL MYGET_CHAR
	POP BC
	DJNZ DONXT
	JP MENU


MYGET_CHAR: CALL OS_CHKCURS
		XOR A
	CALL READCHAR							;GET THE KEY FROM USB KEYBOARD (HAS A TIMEOUT)
	CP 0 								;IF KEY=0 THEN IT IS INVALID
	JR Z, MYGET_CHAR
	;CALL CHKFKEYS_KB
	RET