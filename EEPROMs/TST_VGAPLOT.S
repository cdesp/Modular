

INCLUDE MYOS_2_0000.SYM

ORG $C000

CALL VGA_GRCLS
LD A,VGA_BLUE
LD HL,0
LD DE,$3C
LD B,40
DAGN: PUSH BC
		CALL VGA_PLTPXL
      POP BC
INC HL
INC DE
DJNZ DAGN
LD A,64
LD HL,8
LD DE,$3C
CALL VGA_PRNGRCHAT

LD A,2
LD (VGATX_X),A
LD A,6
LD (VGATX_Y),A
LD A,63
CALL VGA_PRNGRCH

		LD A, 0
		LD (CURSORX), A
		LD (VGATX_X), A
		LD A, 8
		LD (CURSORY), A
		LD (VGATX_Y), A
		LD HL, SMENU
		CALL OS_PRINTTEXT


CALL RS_RX

JP MENU

SMENU DEFM "hello WORLD!@"
 	DB 0