		ORG $C000
		
		
		INCLUDE MYOS_2_0000_HARD.SYM


		
		
		
AGN:		CALL SHOWBINIMG
		CALL GET_CHAR 
		CALL VGA_GETCONF
		LD C,$03		;HIRES TEXT
		CALL VGA_SETCONF
		JP MENU
		
		




IMGNAME		DEFM "ATLAS2.IMG"	;320X200X4BIT IMG 32KB
		DB 0



SHOWBINIMG:	CALL VGA_GETCONF
		LD C,$00		;LOWRES GRAPHICS
		CALL VGA_SETCONF

		LD A,8
		LD B,2
		CALL PUTPGINBANK
		LD A,9
		LD B,3
		CALL PUTPGINBANK
		LD A,10
		LD B,4
		CALL PUTPGINBANK
		LD A,11
		LD B,5
		CALL PUTPGINBANK
		LD HL,$4000

		;LOAD FILE TO $4000
		LD HL,IMGNAME
		LD DE,STRGBUFF
		LD BC,11
		LDIR
		LD A,2			;OPEN FOR READ/WRITE
		CALL STRG_OPNFILE
		RET C			;ERROR
		LD DE,$4000
		LD BC,32000
		PUSH AF
		CALL STRG_BLKREAD
		POP AF
		CALL STRG_CLSFILE	
		RET
		

		
