org $4000

INCLUDE MYOS_2_0000.SYM

	
START:	CALL GET_CHAR
	PUSH AF
	CALL SOUTAS
	POP AF
	;TESTS FOR KEYBOARD FOR F5-F8
	CP 165
	CALL Z,DOF5
	CP 166
	CALL Z,DOF6
	CP 167
	CALL Z,DOF7
	CP 168
	CALL Z,DOF8
	;TESTS FOR SERIAL FOR F5-F8
	CP 3
	CALL Z,DOF5
	CP $0B
	CALL Z,DOF6
	CP $83
	CALL Z,DOF7
	CP $0A
	CALL Z,DOF8

	CALL START


DOF5:	LD HL,MSF5
	CALL OS_PRINTTEXT
	RET	

DOF6:	LD HL,MSF6
	CALL OS_PRINTTEXT
	RET	

DOF7:	LD HL,MSF7
	CALL OS_PRINTTEXT
	RET	

DOF8:	LD HL,MSF8
	CALL OS_PRINTTEXT
	RET	

MSF5:	DEFM " F5 "
	DB 10,13,0

MSF6:	DEFM " F6 "
	DB 10,13,0

MSF7:	DEFM " F7 "
	DB 10,13,0

MSF8:	DEFM " F8 "
	DB 10,13,0