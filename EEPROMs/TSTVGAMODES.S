		ORG $C000
		
		
		INCLUDE MYOS_2_0000_HARD.SYM


		
		
		
AGN:		
		CALL GET_CHAR 

		CP '8'
		CALL Z,TST02

		
		CP '3'
		CALL Z,TST3

		
		
		CP 'M'
		JP Z, MENU
		JR AGN
		








TST02:		LD A,8
		LD B,2
		CALL PUTPGINBANK
		LD A,9
		LD B,3
		CALL PUTPGINBANK
		LD A,10
		LD B,4
		CALL PUTPGINBANK
		LD A,11
		LD B,5
		CALL PUTPGINBANK
		LD HL,$4000
		LD A,$FF;
		LD (HL),A
		;INC HL
		LD DE,$4001
		LD BC,$7FFE
		LDIR
		RET
		


TST3:		LD A,8
		LD B,4
		CALL PUTPGINBANK

		

		LD A, VGA_RED
		LD (VGAFCOL), A
		LD A, VGA_WHITE
		LD (VGABCOL), A
		LD A,0
		LD (VGATX_X),A
		LD A,7
		LD (VGATX_Y),A
		CALL PRNCHRS
		LD A, VGA_GREEN
		LD (VGAFCOL), A
		LD A, VGA_FUCHSIA
		LD (VGABCOL), A
		LD A,1
		LD (VGATX_X),A
		LD A,8
		LD (VGATX_Y),A
		CALL PRNCHRS
		LD A, VGA_BLUE
		LD (VGAFCOL), A
		LD A, VGA_YELLOW
		LD (VGABCOL), A
		LD A,2
		LD (VGATX_X),A
		LD A,9
		LD (VGATX_Y),A
		CALL PRNCHRS

		LD A, VGA_RED
		LD (VGAFCOL), A
		LD A, VGA_BLACK
		LD (VGABCOL), A
		LD A,3
		LD (VGATX_X),A
		LD A,10
		LD (VGATX_Y),A
		CALL PRNCHRS
		LD A, VGA_GREEN
		LD (VGAFCOL), A
		LD A, VGA_BLUE
		LD (VGABCOL), A
		LD A,4
		LD (VGATX_X),A
		LD A,11
		LD (VGATX_Y),A
		CALL PRNCHRS
		LD A, VGA_BLUE
		LD (VGAFCOL), A
		LD A, VGA_GREEN
		LD (VGABCOL), A
		LD A,5
		LD (VGATX_X),A
		LD A,12
		LD (VGATX_Y),A
		CALL PRNCHRS

		RET
				




PRNCHRS:	
				


		LD HL,MYMES1
		CALL OS_PRINTTEXT

		RET

MYMES1		DEFM "HELLO WORLD!!!"
		DB 13,0


		
CLEARSCREEN:	DI			; SET FCOLOR AND BCOLOR DEFAULT VALUES
		 							;LOW RES graph SCREEN
		CALL VGA_SETCONF
		
		LD A, 10
		LD (CURSORX), A
		LD A, 20
		LD (CURSORY), A
		LD HL, MYMSG1
		CALL LCD_MSG
		LD A, 10
		LD (CURSORX), A
		LD A, 10
		LD (CURSORY), A
		LD HL, MYMSG1
		CALL LCD_MSG
		XOR A
		RET

MYMSG1	DEFM "TEST"
	DB 10,13,0
		
